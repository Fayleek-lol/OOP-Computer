class SalesCompany {
    
    private String numberSalesCompany;
    private String nameSalesCompany;
    private String addressSalesCompany;
    
    public void setNumberSalesCompany(String numberSalesCompany){
        this.numberSalesCompany = numberSalesCompany;
    }
    
    public String getNumberSalesCompany(){
        return numberSalesCompany;
    }
    
    public void setNameSalesCompany (String nameSalesCompany){
        this.nameSalesCompany = nameSalesCompany;
    }
    
    public String getNameSalesCompany (){
        return nameSalesCompany;
    }
    
    public void setAddressSalesCompany(String addressSalesCompany){
        this.addressSalesCompany = addressSalesCompany;
    }
    
    public String getAddressSalesCompany(){
        return addressSalesCompany;
    }
    
}
